// Intersection Observer for Animations
const animateOnScroll = () => {
    const elements = document.querySelectorAll('.about-content, .experience-card, .project-card, .skill-category, .contact-content');
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('animate');
                observer.unobserve(entry.target);
            }
        });
    }, {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    });

    elements.forEach(element => {
        observer.observe(element);
    });
};

// Typing Effect for Hero Section
const typeWriter = () => {
    const text = "Web Developer";
    const element = document.querySelector('.hero h2');
    let i = 0;
    const speed = 100;

    const type = () => {
        if (i < text.length) {
            element.textContent += text.charAt(i);
            i++;
            setTimeout(type, speed);
        }
    };

    // Clear the text first
    element.textContent = '';
    type();
};

// Initialize animations and effects when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    animateOnScroll();
    typeWriter();
});

// Add parallax effect to hero section
window.addEventListener('scroll', () => {
    const hero = document.querySelector('.hero');
    const scrolled = window.pageYOffset;
    hero.style.backgroundPositionY = -(scrolled * 0.5) + 'px';
}); 
